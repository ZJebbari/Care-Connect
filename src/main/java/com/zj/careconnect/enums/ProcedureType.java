package com.zj.careconnect.enums;

public enum ProcedureType {
    CONSULTATION,    // Meeting with a medical professional for advice or diagnosis
    INTERVENTION,    // Medical or surgical procedure performed
    DRESSING,        // Dressing wounds
    DIAGNOSTIC_TEST, // Tests like X-rays, MRIs, or blood work
    SURGERY,         // Surgical operations
    PRESCRIPTION,    // Issuing medication
    FOLLOW_UP,       // Follow-up appointments after initial consultation
    EMERGENCY_CARE,  // Emergency medical services
    VACCINATION      // Administering vaccines
}

